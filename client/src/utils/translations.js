// 获取当前语言的翻译
export const getTranslation = (lang, key) => {
  const keys = key.split(".");
  let value = translations[lang] || translations.en;
  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) {
      // 如果找不到，尝试从英文翻译中获取
      value = translations.en;
      for (const k2 of keys) {
        value = value?.[k2];
      }
      break;
    }
  }
  return value || key;
};

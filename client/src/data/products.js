// 根据语言获取本地化的产品数据
export const getLocalizedProduct = (product, language = 'en') => {
  if (!product) return null
  
  // 如果是英文或没有翻译，直接返回原产品
  if (language === 'en' || !productTranslations[product.id]) {
    return product
  }
  
  // 获取中文翻译
  const translation = productTranslations[product.id][language]
  if (!translation) {
    return product
  }
  
  // 合并原产品和翻译
  return {
    ...product,
    ...translation
  }
}